public class EndsWithString {
    public static void main(String[] args) {
        String a = "Akram alhikam";

        System.out.println("Hasilnya:"+a.endsWith("m"));
        System.out.println("Hasilnya:"+a.endsWith("am"));
        System.out.println("Hasilnya:"+a.endsWith("ram"));
        System.out.println("Hasilnya:"+a.endsWith("ikam"));

    }
}
